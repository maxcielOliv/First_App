class Clients {
  late int id;
  late String nome;
  late String telefone;
  late String email;
  late String urlAvatar;

  Clients(
      int id, String nome, String telefone, String email, String urlAvatar) {
    this.id = id;
    this.nome = nome;
    this.telefone = telefone;
    this.email = email;
    this.urlAvatar = urlAvatar;
  }
}
